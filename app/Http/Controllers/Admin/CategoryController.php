    private function getSlug($name)
    {
        $slug = Str::of($name)->slug('-');
        $count = 1;

        while(Category::where('slug' , $slug)->first() ){
            $slug = Str::of($name)->slug('-') . "-{$count}";
            $count++;
        }

        return $slug;
    }
